//3. Working with objects

// Define a car object
let car = {
    type: "Honda",
    model: "Civic",
    color: "Silver"
};

// Check the data type of the car variable
console.log(typeof car); // Output: "object"

// Change the car type
car.type = "Toyota";
// Display the updated car object
console.log(car); 

// Add a new property 'wheels'
car.wheels = 4;
// Display the car object with the added property
console.log(car);

